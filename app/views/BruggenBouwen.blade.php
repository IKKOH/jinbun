<div id="content"><h2>Bruggen bouwen</h2>
<p>川には島がある。ビーバーのボブはここに端を作りたい</p>
<p>川にある島々には全て橋を架けることが可能である。</p>
<p>しかしボブは出来るだけ早く両岸を繋ぎたい</p>
<p>ボブはすでに計画書を作ってある。全ての橋のパターンが書いてあり、近くにある数字はその建設にかかる日数である。</p>
<p><strong>これらはクリックすることで建設する橋を選択することが出来る。最小日数で両岸をつなぐよう建設する橋を選び、ボブを助けよう</strong>&nbsp;</p>

